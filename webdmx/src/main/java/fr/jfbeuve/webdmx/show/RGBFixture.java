package fr.jfbeuve.webdmx.show;


public class RGBFixture {
	int red, green, blue;
	
	public RGBFixture(int red, int green, int blue) {
		super();
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	
	public int red(){
		return red;
	}
	public int green(){
		return green;
	}
	public int blue(){
		return blue;
	}
}
